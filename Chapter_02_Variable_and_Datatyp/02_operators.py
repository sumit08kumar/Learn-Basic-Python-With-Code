a = 34
b = 24

# Arithematic Operator
print("The value of a + b is", a+b)
print("The value of a - b is", a-b)
print("The value of a * b is", a*b)
print("The value of a / b is", a/b)

# Assignment Operator

a = 34
a += 2

b = 40
b -= 20

c = 16
c *= 2

d = 16
d /= 2
print(a)
print(b)
print(c)
print(d)

# Comparison Operator

a = (14 > 7)
print(a)

b = (15 < 5)
print(b)

c = (14 >= 7)
print(c)

d = (14 <= 7)
print(d)

e = (14 == 7)
print(e)

f = (14 != 7)
print(f)

# Logical operators

a = True
b = False
print("The value of a and b is", (a and b))
print("The value of a and b is", (a or b))
print("The value of a and b is", (not b))

