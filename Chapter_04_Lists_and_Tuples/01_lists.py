# Create a list using []
a = [1, 2, 4, 56, 6, 8]

# Print the list using print() function
print(a)

# Access using index using a[0], a[1], a[2]
print(a[2])

# Change th value of list using
a[0] = 98
print(a)

# We can create a list with items of different types
c = [45, "Harry", False,25.5]
print(c)

# List Slicing

friends = ["Sumit", "Sujal", "Udith", "SKS", "Krishana", 50]
print(friends[0:4])

# Negative Slicing
print(friends[-4:4])

