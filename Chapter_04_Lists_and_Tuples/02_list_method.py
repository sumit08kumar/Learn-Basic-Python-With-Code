# Sort the list
L = [1, 8, 7, 2, 21, 15]
L_sorted = L.sort()
print(L)

# Reverses the list
L.reverse()
print(L)

# Appends adds at the end of the list
L.append(45)
print(L)

# Insert the values
L.insert(1, 10)
print(L)

# POP function use to remove elements (Indexing use)
L.pop(2)
print(L)

# Remove use to removes values from list (direct value)
L.remove(8)
print(L)

